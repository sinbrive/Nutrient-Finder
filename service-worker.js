const CACHE_NAME = 'nutrient-finder-cache-v1';
const urlsToCache = [
    './',
    './index.html',
    './styles.css',
    './app.js'
];

self.addEventListener('install', event => {
    event.waitUntil(
        caches.open(CACHE_NAME)
            .then(cache => cache.addAll(urlsToCache))
    );
});

